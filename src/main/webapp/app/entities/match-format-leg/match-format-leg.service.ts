import axios from 'axios';

import { type IMatchFormatLeg } from '@/shared/model/match-format-leg.model';

const baseApiUrl = 'api/match-format-legs';

export default class MatchFormatLegService {
  public find(id: string): Promise<IMatchFormatLeg> {
    return new Promise<IMatchFormatLeg>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: string): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IMatchFormatLeg): Promise<IMatchFormatLeg> {
    return new Promise<IMatchFormatLeg>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IMatchFormatLeg): Promise<IMatchFormatLeg> {
    return new Promise<IMatchFormatLeg>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IMatchFormatLeg): Promise<IMatchFormatLeg> {
    return new Promise<IMatchFormatLeg>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
